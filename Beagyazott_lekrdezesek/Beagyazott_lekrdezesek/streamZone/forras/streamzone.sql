-- StreamZone sample database for subquery practice
-- MySQL 8.x friendly, rerunnable, ASCII identifiers, ID-based foreign keys
SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

DROP DATABASE IF EXISTS streamzone;
CREATE DATABASE streamzone CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE streamzone;

DROP TABLE IF EXISTS comments;
DROP TABLE IF EXISTS playlist_videos;
DROP TABLE IF EXISTS playlists;
DROP TABLE IF EXISTS likes;
DROP TABLE IF EXISTS views;
DROP TABLE IF EXISTS subscriptions;
DROP TABLE IF EXISTS videos;
DROP TABLE IF EXISTS channels;
DROP TABLE IF EXISTS users;

CREATE TABLE users (
  user_id INT NOT NULL,
  username VARCHAR(40) NOT NULL,
  country VARCHAR(40) NOT NULL,
  birth_year INT NOT NULL,
  join_date DATE NOT NULL,
  PRIMARY KEY (user_id),
  UNIQUE KEY uq_users_username (username)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE channels (
  channel_id INT NOT NULL,
  channel_name VARCHAR(80) NOT NULL,
  owner_user_id INT NOT NULL,
  created_year INT NOT NULL,
  PRIMARY KEY (channel_id),
  UNIQUE KEY uq_channels_name (channel_name),
  KEY idx_channels_owner (owner_user_id),
  CONSTRAINT fk_channels_owner
    FOREIGN KEY (owner_user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE videos (
  video_id INT NOT NULL,
  channel_id INT NOT NULL,
  title VARCHAR(150) NOT NULL,
  upload_year INT NOT NULL,
  duration_sec INT NOT NULL,
  PRIMARY KEY (video_id),
  KEY idx_videos_channel (channel_id),
  KEY idx_videos_upload_year (upload_year),
  CONSTRAINT fk_videos_channel
    FOREIGN KEY (channel_id) REFERENCES channels(channel_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE subscriptions (
  user_id INT NOT NULL,
  channel_id INT NOT NULL,
  sub_date DATE NOT NULL,
  PRIMARY KEY (user_id, channel_id),
  KEY idx_subscriptions_channel (channel_id),
  CONSTRAINT fk_subscriptions_user
    FOREIGN KEY (user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_subscriptions_channel
    FOREIGN KEY (channel_id) REFERENCES channels(channel_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE views (
  view_id INT NOT NULL,
  user_id INT NOT NULL,
  video_id INT NOT NULL,
  watch_time_sec INT NOT NULL,
  view_date DATE NOT NULL,
  PRIMARY KEY (view_id),
  KEY idx_views_user (user_id),
  KEY idx_views_video (video_id),
  CONSTRAINT fk_views_user
    FOREIGN KEY (user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_views_video
    FOREIGN KEY (video_id) REFERENCES videos(video_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE likes (
  like_id INT NOT NULL,
  user_id INT NOT NULL,
  video_id INT NOT NULL,
  PRIMARY KEY (like_id),
  UNIQUE KEY uq_likes_user_video (user_id, video_id),
  KEY idx_likes_video (video_id),
  CONSTRAINT fk_likes_user
    FOREIGN KEY (user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_likes_video
    FOREIGN KEY (video_id) REFERENCES videos(video_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE playlists (
  playlist_id INT NOT NULL,
  user_id INT NOT NULL,
  playlist_name VARCHAR(100) NOT NULL,
  PRIMARY KEY (playlist_id),
  KEY idx_playlists_user (user_id),
  CONSTRAINT fk_playlists_user
    FOREIGN KEY (user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE playlist_videos (
  playlist_id INT NOT NULL,
  video_id INT NOT NULL,
  PRIMARY KEY (playlist_id, video_id),
  KEY idx_playlist_videos_video (video_id),
  CONSTRAINT fk_playlist_videos_playlist
    FOREIGN KEY (playlist_id) REFERENCES playlists(playlist_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_playlist_videos_video
    FOREIGN KEY (video_id) REFERENCES videos(video_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE comments (
  comment_id INT NOT NULL,
  user_id INT NOT NULL,
  video_id INT NOT NULL,
  comment_text VARCHAR(250) NOT NULL,
  comment_date DATE NOT NULL,
  PRIMARY KEY (comment_id),
  KEY idx_comments_user (user_id),
  KEY idx_comments_video (video_id),
  CONSTRAINT fk_comments_user
    FOREIGN KEY (user_id) REFERENCES users(user_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_comments_video
    FOREIGN KEY (video_id) REFERENCES videos(video_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

